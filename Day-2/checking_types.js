// Checking data types

console.log(typeof "Radosav"); // string
console.log(typeof 10, typeof 3.14); // number number
console.log(typeof true, typeof false); // boolean boolean
console.log(typeof NaN); // number
console.log(typeof Infinity); // number
console.log(typeof undefined); // undefined
console.log(typeof null); // object

console.log(typeof Symbol); // function
console.log(typeof Object); // function
console.log(typeof Array); // function
console.log(typeof String, typeof Number, typeof Boolean); // function
console.log(typeof []); // object
console.log(typeof BigInt(10)); // bigint
